package studio.styx.erisbot.features.interactions.economy.workSystem;

public class InterviewObject {
    private String answer;
    private String response;

    // Getters e setters
    public String getAnswer() {
        return answer;
    }

    public InterviewObject setAnswer(String answer) {
        this.answer = answer;
        return this;
    }

    public String getResponse() {
        return response;
    }

    public InterviewObject setResponse(String response) {
        this.response = response;
        return this;
    }
}
